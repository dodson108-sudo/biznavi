    text += `• 시장 성장률: ${data.growth}\n`;

    text += `\n▶ 핵심 시장 트렌드:\n`;
    data.keyTrends.forEach((t, i) => { text += `  ${i+1}. ${t}\n`; });

    text += `\n▶ 주요 리스크:\n`;
    data.risks.forEach((r, i) => { text += `  ${i+1}. ${r}\n`; });

    text += `\n▶ 성장 기회:\n`;
    data.opportunities.forEach((o, i) => { text += `  ${i+1}. ${o}\n`; });

    text += `\n▶ 업종 핵심 KPI 벤치마크:\n`;
    data.keyMetrics.forEach((m, i) => { text += `  ${i+1}. ${m}\n`; });

    text += `\n위 데이터를 SWOT 분석, STP 시장 규모, KPI 목표 설정에 구체적으로 반영할 것.\n`;
